#Concatination of string and integer
print("My age is " + str(25))  # Converting integer to string for concatenation

#Addition of integers
print(15 + 10)  # Output: 25

#Subtraction of integers
print(15 - 10)  # Output: 5

#Multiplication of integers
print(15 * 10)  # Output: 150 

#Division of integers
print(15 / 10)  # Output: 1.5 and data type will be float

#implicit type conversion during division
print(15//10)  # Output: 1 and data type will be int

#power raised to
print(2 ** 3)  # Output: 8


